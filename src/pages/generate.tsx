import Head from "next/head";
import React, { useState } from "react";
import { Button } from "~/component/button";
import { api } from "~/utils/api";
import Image from "next/image";
import clsx from "clsx";

export default function Generate() {
  const [form, setForm] = useState({
    prompt: "",
  });

  const [imageData, setImageData] = useState<string>("");

  const generateIcon = api.generate.generateIcon.useMutation({
    onSuccess(data, variables, context) {
      setImageData(data.imageUrl);
    },
  });

  function submitForm(e: React.FormEvent) {
    e.preventDefault();
    generateIcon.mutate({
      prompt: form.prompt,
    });
    setForm({
      prompt: "",
    });
  }

  function updateForm(key: string) {
    return function (e: React.ChangeEvent<HTMLInputElement>) {
      setForm({
        ...form,
        [key]: e.target.value,
      });
    };
  }

  return (
    <>
      <Head>
        <title>Generate avatars</title>
        <meta name="description" content="Generated by create-t3-app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <div className="container mx-auto flex h-screen w-full flex-col">
        <div className="flex flex-col gap-5 pt-24">
          <h1 className="text-4xl">Generate some avatars</h1>
          <p>Fill the form below with your prompt and hit Submit</p>
          <form
            onSubmit={(e) => submitForm(e)}
            className="flex h-fit flex-col gap-4 pt-12"
          >
            <h2>1. Describe what you want your avatar look like</h2>
            <div className="flex flex-col gap-2">
              <label className="text-sm">Prompt</label>
              <input type="text" onChange={updateForm("prompt")} />
              <Button disabled={generateIcon.isLoading}>Submit</Button>
            </div>
          </form>

          <div className="flex w-full flex-col items-center gap-2">
            {imageData && (
              <>
                <h2>2. Your avatars</h2>
                <Image
                  src={imageData}
                  alt="The image that was generated by your prompt"
                  width="512"
                  height="512"
                />
              </>
            )}
          </div>
        </div>
      </div>
    </>
  );
}
